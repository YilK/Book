'''
使用一个字典来存储一个熟人的信息，包括名、姓、年龄和居住的城市。
该字典应包含键first_name 、last_name 、age 和city 。将存储在该字典中 的每项信息都打印出来
'''
message = {
    'first_name': 'huang',
    'last_name': 'junkai',
    'age': 21,
    'city': 'zhejiang'
}
